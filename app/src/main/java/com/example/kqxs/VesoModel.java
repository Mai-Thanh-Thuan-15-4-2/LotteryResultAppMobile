package com.example.kqxs;

public class VesoModel {
    private int mavs;
    private String dayso;
    private String tentinh;
    private String ngayxo;
    private int soluong;

    public VesoModel() {
        this.mavs = mavs;
        this.dayso = dayso;
        this.tentinh = tentinh;
        this.ngayxo = ngayxo;
        this.soluong = soluong;
    }

    public int getMavs() {
        return mavs;
    }

    public void setMavs(int mavs) {
        this.mavs = mavs;
    }

    public String getDayso() {
        return dayso;
    }

    public void setDayso(String dayso) {
        this.dayso = dayso;
    }

    public String getTentinh() {
        return tentinh;
    }

    public void setTentinh(String tentinh) {
        this.tentinh = tentinh;
    }

    public String getNgayxo() {
        return ngayxo;
    }

    public void setNgayxo(String ngayxo) {
        this.ngayxo = ngayxo;
    }

    public int getSoluong() {
        return soluong;
    }

    public void setSoluong(int soluong) {
        this.soluong = soluong;
    }
}
